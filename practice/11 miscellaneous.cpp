

// about include

// "#include" is a preprocessor directive used to include a header file in your C++ program. A header file is a file that contains declarations of functions and variables that can be used in your program. When you use #include <header_file>, the preprocessor replaces that line with the contents of the specified header file before the actual compilation of your code.

// in simple words "#include" is a preprocessor directive which includes the header files specified in main code before compilation

// For example, when you include #include <iostream>, the preprocessor includes the contents of the iostream header file, which contains the declarations of standard input/output functions like cout and cin. This allows you to use these functions in your program without having to write their declarations yourself.

